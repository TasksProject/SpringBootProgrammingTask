package Aufgabe;

public class Multiplier {
    private int a;
    private int b;
    private int result;

    public Multiplier(int a, int b) {
        this.a = a;
        this.b = b;
    }

    public int getA() {
        return a;
    }

    public int getB() {
        return b;
    }

    public int getResult() {
        return result = a*b;
    }
}
